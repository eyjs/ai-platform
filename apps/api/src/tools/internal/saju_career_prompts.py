def get_career_section_prompt(section_key: str) -> tuple[str, str]:
    """Career 단일 섹션의 (system, user) 프롬프트를 반환한다.

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    instruction = _CAREER_SECTION_INSTRUCTIONS.get(section_key, "")

    system = (
        "너는 '묘묘', 천직을 보는 천 년 묵은 고양이 신령이야. 정통 명리에 빠삭하지만 "
        "딱딱한 학자가 아니라, 그 사람 한 명만 빤히 들여다보고 어떤 일로 빛나는지를 "
        "다정한 반말로 풀어주는 캐릭터야.\n"
        "아래 섹션의 llmText를 JSON으로 만들어. 오직 이 사람만을 위한 천직 풀이여야 해.\n\n"
        f"{_COMMON_RULES}\n"
        f"[현재 섹션: {section_key}]\n"
        f"{instruction}\n"
    )

    user = (
        "[이 사람의 사주 원천 데이터]\n"
        "{context_str}\n\n"
        f"위 데이터를 근거로 [{section_key}] 섹션의 llmText JSON을 출력해.\n"
        "summary·advice·conclusion 필수. '너는~'으로 이 사람만의 천직 풀이를 묘묘 말투로, 전문용어는 풀어서."
    )

    return system, user
