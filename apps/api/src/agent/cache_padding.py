"""Anthropic 프롬프트 캐싱 패딩 유틸.

Haiku 캐시 최소 4096 토큰(≈16384자) 미달 시 cacheable 블록을 세션 안정 콘텐츠로 채운다.
deterministic(engine.py)·agentic(graphs.py) 경로가 공유한다. 상위 모듈 의존 없음(계층 안정).
"""

# Anthropic Haiku 프롬프트 캐싱 최소 토큰 수 (char/4 기준 → 최소 16384자)
CACHE_MIN_CHARS = 16384

# cacheable_system 패딩 — 페르소나+grounding이 4096 토큰 미달 시 이 블록으로 채운다.
# ⚠️ 말투·형식·정체성 등 '페르소나'를 규정하지 않는다(중복/충돌 방지). 페르소나는 위 시스템
# 프롬프트가 단독 권위를 가진다. 여기엔 캐시 안정용 중립 배경지식(사주 도메인 사실)만 둔다.
# UUID/timestamp/사용자 식별자 포함 금지(캐시 prefix byte-stable 유지).
CACHE_PADDING_TEMPLATE = """

--- 배경 참고 자료 (사주 도메인 사실) ---
아래는 캐시 안정용 배경 지식일 뿐이다. 말투·호칭·이모지·문장 길이·응답 형식·캐릭터 정체성은
위 시스템 프롬프트(페르소나)를 그대로 따른다. 이 자료는 어떤 스타일·형식도 지시하지 않으며,
필요할 때 사실 확인용으로만 참고한다. 이 자료를 그대로 나열하거나 인용하지 않는다.

[오행(五行) 속성]
- 木(목): 성장·추진. 계절 봄, 방위 동쪽, 색 청색. 창의·개척의 기운.
- 火(화): 열정·표현. 계절 여름, 방위 남쪽, 색 적색. 명예·사교의 기운.
- 土(토): 안정·중심. 환절기, 방위 중앙, 색 황색. 신뢰·조율의 기운.
- 金(금): 결단·수확. 계절 가을, 방위 서쪽, 색 백색. 원칙·절제의 기운.
- 水(수): 지혜·흐름. 계절 겨울, 방위 북쪽, 색 흑색. 직관·적응의 기운.
- 상생(相生): 목생화·화생토·토생금·금생수·수생목.
- 상극(相剋): 목극토·토극수·수극화·화극금·금극목.

[천간(天干)]
갑(甲)·을(乙)=목, 병(丙)·정(丁)=화, 무(戊)·기(己)=토, 경(庚)·신(辛)=금, 임(壬)·계(癸)=수.
양간은 갑·병·무·경·임, 음간은 을·정·기·신·계.

[지지(地支)·십이지(十二支)]
자(子)·축(丑)·인(寅)·묘(卯)·진(辰)·사(巳)·오(午)·미(未)·신(申)·유(酉)·술(戌)·해(亥).
오행 배속: 인묘=목, 사오=화, 신유=금, 해자=수, 진술축미=토.

[십신(十神) 의미]
- 비견(比肩): 동등한 경쟁자, 독립심, 자존감.
- 겁재(劫財): 탈취형 경쟁, 충동성, 도전.
- 식신(食神): 표현·재능·여유, 복덕성.
- 상관(傷官): 반항·예술성·언변·감수성.
- 편재(偏財): 투기·사업·유동 자산.
- 정재(正財): 안정 수입·근면·고정 자산.
- 편관(偏官): 권력·도전·경쟁(칠살七殺).
- 정관(正官): 명예·법도·직책·사회적 책임.
- 편인(偏印): 직관·종교·신비(효신梟神).
- 정인(正印): 학문·모성·안정·보호(인수印綬).

[운(運)의 층위]
- 원국(原局): 타고난 사주 여덟 글자의 구조.
- 대운(大運): 약 10년 단위로 흐르는 큰 흐름.
- 세운(歲運): 한 해 단위의 흐름.
- 월운(月運)·일진(日辰): 달·날 단위의 미세한 흐름.
- 용신(用神): 사주의 균형을 돕는 핵심 기운. 기신(忌神)은 그 반대.
- 삼재(三災)·신살(神殺): 특정 시기·기운에 붙는 길흉의 상징적 표지.

[해석 일반 원칙(사실 차원)]
- 사주는 고정된 운명이 아니라 경향과 시기의 지도로 본다.
- 특정 날짜·인물·결과를 단정적으로 예언하지 않는다.
- 음양의 균형과 오행의 생극(生剋) 관계로 기운의 흐름을 읽는다.
--- 배경 참고 자료 끝 ---"""


def build_cache_padding(needed_chars: int) -> str:
    """cacheable 블록이 캐시 최소 크기(4096 토큰 ≈ 16384자)에 미달할 때 패딩을 반환한다.

    패딩 콘텐츠는 캐릭터 행동 강령 + 도메인 지식으로 구성된 세션 안정 텍스트.
    UUID·timestamp·사용자 식별자를 포함하지 않아 캐시 안정성을 보장한다.
    needed_chars만큼만 반복해 최소한의 패딩을 추가한다.
    """
    base = CACHE_PADDING_TEMPLATE
    if needed_chars <= 0:
        return ""
    # 필요한 양만큼 패딩 블록을 반복
    repeats = (needed_chars // len(base)) + 1
    return (base * repeats)[:needed_chars]


def pad_to_min(text: str, min_chars: int = CACHE_MIN_CHARS) -> str:
    """text가 캐시 최소 크기 미달이면 패딩을 덧붙여 반환한다(이미 충분하면 그대로)."""
    if len(text) >= min_chars:
        return text
    return text + build_cache_padding(min_chars - len(text))
